#include<stdio.h>
#include<math.h>
int main()
{
	// ceil() 
	printf("Ceil = %f\n", ceil(4.6));
	// floor()
	printf("Floor = %f\n", floor(4.6));
	// fabs()
	printf("Fabs = %f\n", fabs(-2.0));
	// sqrt()
	printf("sqrt = %f\n", sqrt(16));
	// pow()
	printf("Power = %f\n", pow(2,4));
	// log()
	printf("Log = %f\n", log(4.0));
	// log10()
	printf("Log10 = %f\n", log10(100));
	// exp()
	printf("Exp = %f\n", exp(4.0));
	// cos()
	// printf("Cosine = %f\n", cos(30));
	return 0;
}
